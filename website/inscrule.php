<h2 class="page-title">Rules and Regulations</h2>

<div class="post-content"><p><span class="dropcap">R</span>ead the following rules carefully. All teams must comply with them. Else the team could be banned.</p>

<ul>

<li>This is a Team event. A team can consist of maximum 3 people.</li>

<li>Participants have to register their team on the Engineer Site before participating.</li>

<li>All Solutions are evaluated by an automated judge. Hence requests for re-evaluation are strictly not considered.</li>

<li>All teams have to submit their source code to our judge.</li>

<li>Using unfair means, Or attempts to defeat the system shall attract summary dis-qualification of the respective teams.</li>

<li>Coding Rules and Regulations</li>

<ul>

<li>Use of systemcalls, attempts to openfiles/connect to network are not allowed. such solutions will be disqualified.And such repeated attempts can disqualify a team.</li>

<li>Read the input from standard input (STDIN) and write to standard output(STDOUT), using scanf/cin to read and printf/cout to write.</li>

<li> All C and CPP code must return 0 from the main function.</li>
<li> there is a "javaID" given to each problem.Your java submission file should be named after it.Eg if the problem's javaID is one.java, your programs public class should be "one" and thus the file one.java</li>

